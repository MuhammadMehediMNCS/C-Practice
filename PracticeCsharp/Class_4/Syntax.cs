// Syntax মানে হলো প্রোগ্রাম লেখার নিয়ম বা কাঠামো
using System;

class SyntaxRules
{
    static void Main(string[] args)
    {
        Console.WriteLine("Hello, World!"); // এটি একটি আউটপুট স্টেটমেন্ট
    }
}

/*
ব্যাখ্যা :
using System; → এখানে আমরা System লাইব্রেরি ব্যবহার করছি (Console ব্যবহারের জন্য দরকার)

class Program { ... } → সি# প্রোগ্রাম সবসময় ক্লাসের ভেতরে থাকে।

static void Main(string[] args) → মেইন মেথড হচ্ছে প্রোগ্রামের শুরু বিন্দু।

Console.WriteLine("Hello, World!"); → কনসোলে লেখা দেখায়। (সেমিকোলন ; দিতে ভুল করা যাবে না)
*/