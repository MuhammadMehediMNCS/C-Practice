// for loop ব্যবহার করে ১-১০ পর্যন্ত সংখ্যাগুলো প্রিন্ট করুন

using System;

class Program
{
    static void Main(string[] args)
    {
        // for loop ব্যবহার করে ১ থেকে ১০ পর্যন্ত সংখ্যা প্রিন্ট
        for (int i = 1; i <= 10; i++)
        {
            Console.WriteLine(i);
        }
    }
}
